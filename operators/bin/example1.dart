void main(List<String> args) {
  const age1 = 64;
  const age2 = 24;
  print(age1 + age2); // 88
  print(age1 + age2 + 30); // 118
  print(age1 - age2); // 40
  print(age1 * age2); // 1536

  // float
  const divided = age1 / age2;
  print(divided); // // 2.6666

  // int
  const intDivided = age1 ~/ age2;
  print(intDivided); // 2
}
