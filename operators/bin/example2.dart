void main(List<String> args) {
  // 4 types of operators
  // unary prefix,unary postfix, binary infix, compound assignment

  // unary prefix
  int age3 = 30;
  int age4 = --age3;
  print(--age3); // 29
  print(age4); // 29

  print(!true); // false

  print(++age4); // 30

  print(~1); // -2

  print(-age4); // -30
}
